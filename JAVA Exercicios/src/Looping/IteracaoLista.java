package Looping;

public class IteracaoLista {
    public static void main(String[] args) {
        String[] frutas= {"Banana", "Pera", "Uva", "Morango", "Caju", "Abacaxi", "Melância", "Laranja", "Maçã", "Ameixa"};
        for (String fruta : frutas){
            System.out.println(fruta);
        }
    }
}
